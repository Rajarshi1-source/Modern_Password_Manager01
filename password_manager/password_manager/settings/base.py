# Audit-fix M1: production guard on USE_REDIS_CHANNELS
# =============================================================================
# Runs at the very bottom of settings so the TESTING override above has
# already had a chance to force InMemoryChannelLayer for pytest. We only
# fail closed when:
#   * we're not in DEBUG mode, AND
#   * we're not running tests, AND
#   * we're not running a Django management command that just imports
#     settings to inspect them (`check`, `migrate`, `makemigrations`,
#     `collectstatic`, etc.), AND
#   * USE_REDIS_CHANNELS is not 'True'
#
# The third clause is what fixes the CI break (PR #273 review): CI runs
# `python manage.py check --deploy` which imports settings under
# DEBUG=False without USE_REDIS_CHANNELS=True — neither a real prod
# deploy nor a test invocation. The maintenance-mode bypass lets those
# checks pass; the guard still fires on real WSGI/ASGI bootstrap
# (gunicorn / daphne don't run via manage.py).
_DJANGO_MAINTENANCE_COMMANDS = frozenset({
    'check', 'migrate', 'makemigrations', 'showmigrations', 'sqlmigrate',
    'collectstatic', 'compilemessages', 'makemessages',
    'shell', 'shell_plus', 'dbshell',
    'createsuperuser', 'changepassword',
    'dumpdata', 'loaddata',
    'diffsettings', 'inspectdb',
    'startapp', 'startproject',
    'verify_audit_migration', 'rehash_pending_commitments',
})
def _is_django_entrypoint(arg0: str) -> bool:
    """Detect Django CLI entrypoints.

    Audit-trail (PR #273 review, CodeRabbit): the original check
    only matched `manage.py`. `python -m django` (used in some CI
    images and dev workflows) sets argv[0] to a path ending in
    `django/__main__.py` or just `django`, so we widen the predicate.

    PR #285 review (Codex P2): also recognize the ``django-admin``
    console script (the documented entrypoint used by e.g.
    ``django-admin check --deploy`` / ``django-admin migrate`` in
    production-like CI), which was previously missed.
    """
    import os.path
    base = os.path.basename(arg0)
    return (
        arg0.endswith('manage.py')
        or base == 'django'
        or base in ('django-admin', 'django-admin.py')
        or base == '__main__.py' and 'django' in arg0
    )


